    private void generateModuleRows(){
        int id = 0;
        for (Module m : mTemplate.getDistModel().getModuleMap().values())
            ((DefaultTableModel)moduleNameTable.getModel()).addRow(new Object[]{id++, 
                m.getName(), m.getPartitionId()});
    }
    
    
