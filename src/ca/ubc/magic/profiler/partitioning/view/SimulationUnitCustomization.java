    private void addUnitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUnitButtonActionPerformed
        StringBuilder builder = new StringBuilder();
        for (int i =0; i < ((DefaultTableModel)moduleNameTable.getModel()).getRowCount(); i++){
            Integer partitionId = ((Integer)((DefaultTableModel)moduleNameTable.getModel()).getValueAt(i, 2));
            if (partitionId == -1)
                builder.append(Constants.DONT_CARE_CHAR);
            else
                builder.append(Integer.toString(partitionId));
        }
        SimulationUnit unit = SimulationFrameworkHelper.getUnitFromSig(builder.toString(), mTemplate, Boolean.FALSE);
        mSimFramework.addUnit(unit, Boolean.FALSE);
        this.setVisible(Boolean.FALSE);
    }//GEN-LAST:event_addUnitButtonActionPerformed

